PERM_MODES = ("plan", "edit", "auto")


def mode_matrix(*modes: str):
    """`@mode_matrix()` parametrizes over permission modes.

    Default (`mode_matrix()`) covers all three modes. Pass explicit modes to
    restrict, e.g. `mode_matrix("plan", "edit")`.
    """
    chosen = modes or PERM_MODES
    return pytest.mark.parametrize("mode", chosen)


# ---------------------------------------------------------------------------
# JSON shape validation
# ---------------------------------------------------------------------------


def assert_json_shape(payload: dict[str, Any], schema: type) -> Any:
    """Validate `payload` against a pydantic BaseModel `schema`.

    Returns the parsed model instance. Re-raises pydantic ValidationError as
    AssertionError for cleaner pytest reporting.
    """
    try:
        return schema.model_validate(payload)
    except Exception as e:  # noqa: BLE001 — surface as test failure
        raise AssertionError(
            f"JSON payload does not match {schema.__name__}: {e}\n"
            f"payload: {payload!r}"
        ) from e


