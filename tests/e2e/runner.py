def new_session_id() -> str:
    """Stable-format session id (mirrors SessionRecord.new shape)."""
    return uuid.uuid4().hex[:12]


def iter_sessions(project_root: Path) -> Iterator[Path]:
    """Yield session JSON files under <project_root>/.voss/sessions/."""
    sessions_dir = project_root / ".voss" / "sessions"
    if not sessions_dir.is_dir():
        return
    yield from sorted(sessions_dir.glob("*.json"))
