# Golden transcript record/replay
# ---------------------------------------------------------------------------


_TRANSCRIPTS_DIR = Path(__file__).resolve().parent / "transcripts"


def record_or_assert_transcript(
    name: str,
    payload: Any,
    *,
    update: bool,
) -> None:
    """Compare `payload` to the recorded transcript at transcripts/<name>.json.

    If `update` is True (driven by the `--update-transcripts` pytest flag),
    overwrite the file. Otherwise diff and raise AssertionError on mismatch.
    """
    path = _TRANSCRIPTS_DIR / f"{name}.json"
    serialized = json.dumps(payload, indent=2, sort_keys=True, default=str)
    if update or not path.exists():
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(serialized + "\n")
        return
    expected = path.read_text().rstrip("\n")
    if expected != serialized:
        raise AssertionError(
            f"transcript mismatch for {name!r}.\n"
            f"--- expected ({path}) ---\n{expected}\n"
            f"--- actual ---\n{serialized}\n"
            f"run with --update-transcripts to refresh."
        )


